"""
This modules consists tools for counting accuracy.

Functions:
    accuracy_test: This function count answers accuracy.
"""


########################################################################################################################
# M A I N   F U N C T I O N :  #########################################################################################
########################################################################################################################


def accuracy_test(real_answers, fpga_answers):
    """
    This function count answers accuracy.
    """
    counter_all = 0
    counter_true = 0
    for real, fpga in zip(real_answers.tolist(), fpga_answers):
        if str(real) == str(fpga):
            counter_true += 1
        counter_all += 1
    return counter_true / counter_all * 100


