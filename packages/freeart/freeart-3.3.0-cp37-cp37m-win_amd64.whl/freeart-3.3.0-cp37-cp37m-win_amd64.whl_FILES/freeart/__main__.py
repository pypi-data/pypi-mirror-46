"""This module describe freeart applications which are available  through
the silx launcher.

Your environment should provide a command `freeart`. You can reach help with
`freeart --help`, and check the version with `freeart --version`.
"""

__authors__ = ["V. Valls", "P. Knobel", "H. Payno"]
__license__ = "MIT"
__date__ = "24/09/2017"


import logging
logging.basicConfig()

import sys
from silx.utils.launcher import Launcher
import freeart._version


def main():
    """Main function of the launcher

    This function is referenced in the setup.py file, to create a
    launcher script generated by setuptools.

    :rtype: int
    :returns: The execution status
    """
    launcher = Launcher(prog="freeart", version=freeart._version.version)
    launcher.add_command("interpreter",
                         module_name="freeart.app.interpreter",
                         description="Run sinograms reconstructions from a "
                                     "configuration file and for a given number"
                                     " of iteration")
    launcher.add_command("reconsfluosino",
                         module_name="freeart.app.reconsfluosino",
                         description="Reconstruction from a fluorescence "
                                     "sinogram and absorption and self "
                                     "attenuation matrices")
    launcher.add_command("createfluosino",
                         module_name="freeart.app.createfluosino",
                         description="Build a sinogram from a phantom, an"
                                     "absorption matrix and a self absorption "
                                     "matrix")
    launcher.add_command("test",
                         module_name="freeart.app.test_",
                         description="Launch freeart unittest")
    status = launcher.execute(sys.argv)
    return status


if __name__ == "__main__":
    # executed when using python -m PROJECT_NAME
    status = main()
    sys.exit(status)
